package com.ericsson.oss.apps.model.entities;

import jakarta.persistence.Column;
import jakarta.persistence.Entity;
import jakarta.persistence.Id;
import lombok.Getter;
import lombok.NoArgsConstructor;
import lombok.Setter;

import java.util.ArrayList;
import java.util.List;

@Entity
@Getter
@Setter
@NoArgsConstructor
public class Subscription {
    @Id
    @Column(name = "rop_time", nullable = false)
    private Long ropTime;

    private List<String> eNodebList = new ArrayList<>();
    private List<String> nrNodeList = new ArrayList<>();

    public Subscription(Long ropTime) {
        this.ropTime = ropTime;
    }
}
